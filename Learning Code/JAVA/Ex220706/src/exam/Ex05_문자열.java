package exam;

public class Ex05_문자열 {

	public static void main(String[] args) {
		
		System.out.println("Hello World");
		
		// 5. 문자열 String - 클레스에 대한 레퍼런스 타입
		String h2 = "Lunch Time";
		System.out.println(h2);
		
		// 정수형 변수 age에 21을 담아
		// 그 age를 출력하시오!!
		int age = 21;
		System.out.println(age);
		
		char abc = '가';
		int num = (int)abc;
		System.out.println(num);	// 유니코드 숫자 표시

	}

}
