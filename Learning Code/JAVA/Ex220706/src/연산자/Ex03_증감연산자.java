package 연산자;

public class Ex03_증감연산자 {

	public static void main(String[] args) {
		
		// ++, --
		int a = 1;
//		a++;	// a = a + 1
//		++a;
//		a--;	// a = a - 1
//		--a; 
		
		System.out.println(a);
		System.out.println(a++);
		System.out.println(a);
		System.out.println(++a);
		System.out.println(a);
		System.out.println(a--);
		System.out.println(a);
		System.out.println(--a);
	}

}
