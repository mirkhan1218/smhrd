package 연산자;

public class Ex04_비교연산자 {

	public static void main(String[] args) {
		
		// >, >=, <, <=, ==, !=
		int num1 = 4;
		int num2 = 7;
		
		System.out.println(num1>num2);
		System.out.println(num1>=num2);
		System.out.println(num1<num2);
		System.out.println(num1<=num2);
		System.out.println(num1==num2);
		System.out.println(num1!=num2);
		
	}

}
