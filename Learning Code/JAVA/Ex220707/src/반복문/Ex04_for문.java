package 반복문;

public class Ex04_for문 {

	public static void main(String[] args) {
		
		// 조건 3가지 : 초기화 (시작점)
		//            조건식 (도착점)
		//            반복 후 작업 (시작점 -> 도착점 보폭)
		
		// 96 - 51
		// 시작점 : 96
		// 도착점 : 51
		// 보폭 : -1
		for (int i = 96; i >= 51; i--) {
			System.out.print(i + " ");
		}
		
	}

}
